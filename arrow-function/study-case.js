const Pertambahan = (angka1, angka2) => angka1 + angka2;
console.log(Pertambahan(3, 2));

const Pengurangan = (angka1, angka2) => angka1 - angka2;
console.log(Pengurangan(8, 2));

const Perkalian = (angka1, angka2) => angka1 * angka2;
console.log(Perkalian(6, 5));

const Pembagian = (angka1, angka2) => angka1 / angka2;
console.log(Pembagian(14, 2));